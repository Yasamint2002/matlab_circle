% ایجاد یک شیء از تصویر با ابعاد 400x400 پیکسل و پس زمینه سیاه
img = zeros(400, 400, 3);
img(:, :, 1) = 0; % رنگ قرمز
img(:, :, 2) = 0; % رنگ سبز
img(:, :, 3) = 0; % رنگ آبی

% مختصات مرکز دایره
center_x = 200; % نصف عرض تصویر
center_y = 200; % نصف ارتفاع تصویر
radius = 100; % شعاع دایره
circle_color = [0, 0, 1]; % رنگ دایره (آبی)

% ایجاد دایره در تصویر
for y = 1:400
    for x = 1:400
        if ((x - center_x)^2 + (y - center_y)^2 <= radius^2)
            img(y, x, :) = circle_color;
        end
    end
end

% نمایش تصویر
imshow(img);
